        // Get references to all the cells you want to make copyable
var copyCells = document.querySelectorAll(".copyCell");

// Add a click event listener to each copyable cell
copyCells.forEach(function (cell) {
    cell.addEventListener("click", function () {
        // Create a temporary input element
        var tempInput = document.createElement("textarea");
        tempInput.value = cell.textContent; // Set the value to the cell's text content
        document.body.appendChild(tempInput);

        // Select the text inside the input element
        tempInput.select();
        tempInput.setSelectionRange(0, 99999); // For mobile devices

        // Copy the selected text to the clipboard
        document.execCommand("copy");

        // Remove the temporary input element
        document.body.removeChild(tempInput);

        // Provide feedback to the user (optional)
        cell.innerHTML = "Copied!";

        // Reset the cell content after a brief delay (optional)
        setTimeout(function () {
            cell.innerHTML = tempInput.value;
        }, 2000); // 2 seconds
    });
});

</script>
